package com.val.mydrills.gson;

public class SimpleUser {
    String name;
    String mail;
    int age;
    boolean isDeveloper;

    SimpleUser(String name,
               String mail,
               int age,
               boolean isDeveloper){
        this.name = name;
        this.mail = mail;
        this.age = age;
        this.isDeveloper = isDeveloper;
    }

}
